# =============================================
# 2Ô∏è‚É£ Import Required Libraries
# =============================================
import requests
import json

# =============================================
# 3Ô∏è‚É£ Call the API
# =============================================
API_URL = "https://jsonplaceholder.typicode.com/posts/1"

response = requests.get(API_URL)
data = response.json()

print("‚úÖ API response fetched successfully:")
print(data)

# =============================================
# 4Ô∏è‚É£ Save Results as JSON
# =============================================
with open("results.json", "w") as f:
    json.dump(data, f, indent=4)

print("\nüíæ Saved results to results.json")

# =============================================
# 5Ô∏è‚É£ Generate Markdown Report
# =============================================
markdown_text = f"""
# üß™ Mini API Demo ‚Äì JSONPlaceholder

| **Field** | **Value** |
|-----------|-----------|
| User ID   | {data['userId']} |
| Post ID   | {data['id']} |
| Title     | {data['title']} |
| Body      | {data['body'][:50]}... |

‚úÖ Results also saved in `results.json`
"""

with open("API_Demo_Report.md", "w") as f:
    f.write(markdown_text)

print("\nüìÑ Markdown report created: API_Demo_Report.md")
print("\n‚ú® Demo complete! Open 'API_Demo_Report.md' to view your Markdown summary.")
print("\n---\nMarkdown Preview:\n")
print(markdown_text)

